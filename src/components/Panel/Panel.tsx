export type PanelSize = 's' | 'm';

// Set once on <Panel>; header, title, body and footer read it so consumers
// configure everything (size, collapse) in one place instead of wiring props
// through the tree. The chevron toggle renders inside PanelHeader — the
// header is what remains visible when a panel is collapsed, and it works
// with any header content, title or not.
interface PanelContextValue {
  size: PanelSize;
  collapsible: boolean;
  collapsed: boolean;
  onToggle: () => void;
  // Generated by Panel; PanelTitle attaches it to its text so the toggle can
  // borrow the title as its accessible name (see the toggle's aria-labelledby).
  labelId: string;
}

const PanelContext = createContext<PanelContextValue>({
  size: 'm',
  collapsible: false,
  collapsed: false,
  onToggle: () => {},
  labelId: '',
});

