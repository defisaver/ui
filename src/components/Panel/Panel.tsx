interface PanelSectionProps extends ComponentPropsWithoutRef<'div'> {
  children: ReactNode;
}

interface PanelTitleProps extends ComponentPropsWithoutRef<'span'> {
  children: ReactNode;
}

// Collapse comes in two flavors, and the union makes half-wired states
// unrepresentable (`collapsed` without `onToggle` is a compile error):
// - Uncontrolled (the common case): pass `collapsible` (or `defaultCollapsed`,
//   which implies it) and Panel owns the state — PanelBody hides itself.
// - Controlled (escape hatch): pass `collapsed` + `onToggle` together and the
//   consumer owns the state (persistence, expand-all, etc.).
interface UncontrolledCollapseProps {
  collapsible?: boolean;
  defaultCollapsed?: boolean;
  collapsed?: never;
  onToggle?: never;
}

interface ControlledCollapseProps {
  collapsed: boolean;
  onToggle: () => void;
  collapsible?: never;
  defaultCollapsed?: never;
}

// Omit the native onToggle (the <details> toggle event) — on Panel that name
// belongs to the collapse API.
type PanelRootProps = Omit<ComponentPropsWithoutRef<'div'>, 'onToggle'> & {
  children: ReactNode;
  size?: PanelSize;
} & (UncontrolledCollapseProps | ControlledCollapseProps);

// stylex.props() produces the atomic class list (and sometimes inline vars);
// fold the caller's className/style in after it so external overrides
// survive the merge.
const mergeExternal = (
  sx: ReturnType<typeof stylex.props>,
  className?: string,
  style?: CSSProperties,
) => ({
  ...sx,
  className: [sx.className, className].filter(Boolean).join(' '),
  ...(sx.style || style ? { style: { ...sx.style, ...style } } : null),
});

// Private to Panel — a visual detail of the component, not a public icon.
// Rendered with currentColor so it follows the toggle's token color.
// Default (expanded) points down; collapsed rotates to point right.
const Chevron = ({ size, collapsed }: { size: PanelSize; collapsed: boolean }) => {
  const px = size === 'm' ? 16 : 14;
  return (
    <svg
      width={px}
      height={px}
      viewBox="0 0 12 12"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      aria-hidden="true"
      {...stylex.props(styles.chevron, collapsed && styles.chevronCollapsed)}
    >
      <path d="M9 5L6.09642 7.90358C6.04317 7.95683 5.95683 7.95683 5.90358 7.90358L3 5" stroke="currentColor" strokeLinecap="round" />
    </svg>

  );
};

export const Panel = forwardRef<HTMLDivElement, PanelRootProps>(({
  className,
  style,
  children,
  size = 'm',
  collapsible = false,
  defaultCollapsed,
  collapsed: controlledCollapsed,
  onToggle,
  ...rest
}, ref) => {
  const [ownCollapsed, setOwnCollapsed] = useState(defaultCollapsed ?? false);
  const toggleOwn = useCallback(() => setOwnCollapsed((c) => !c), []);
  const labelId = useId();

  const isControlled = controlledCollapsed !== undefined;
  const context = useMemo(() => ({
    size,
    collapsible: isControlled || collapsible || defaultCollapsed !== undefined,
    collapsed: isControlled ? controlledCollapsed : ownCollapsed,
    onToggle: onToggle ?? toggleOwn,
    labelId,
  }), [
    size, isControlled, collapsible, defaultCollapsed,
    controlledCollapsed, ownCollapsed, onToggle, toggleOwn, labelId,
  ]);

  return (
    <PanelContext.Provider value={context}>
      <div ref={ref} {...rest} {...mergeExternal(stylex.props(styles.panel), className, style)}>
        {children}
      </div>
    </PanelContext.Provider>
  );
});
Panel.displayName = 'Panel';

export const PanelHeader = forwardRef<HTMLDivElement, PanelSectionProps>(({
  className, style, children, ...rest
}, ref) => {
  const {
    size, collapsible, collapsed, onToggle, labelId,
  } = useContext(PanelContext);
  return (
    <div
      ref={ref}
      {...rest}
      {...mergeExternal(
        stylex.props(
          styles.header,
          size === 'm' ? styles.headerM : styles.headerS,
          collapsible && (size === 'm' ? styles.headerCollapsibleM : styles.headerCollapsibleS),
          collapsed && styles.headerCollapsed,
        ),
        className,
        style,
      )}
    >
      {collapsible && (
        // Named by the title text when a PanelTitle is present (labelledby
        // pointing at nothing is skipped), otherwise the aria-label fallback
        // applies — so title-less headers still get a usable name.
        <button
          type="button"
          aria-expanded={!collapsed}
          aria-label="Toggle panel"
          aria-labelledby={labelId}
          onClick={onToggle}
          {...stylex.props(styles.toggle, size === 'm' ? styles.toggleM : styles.toggleS)}
        >
          <Chevron size={size} collapsed={collapsed} />
        </button>
      )}
      {children}
    </div>
  );
});
PanelHeader.displayName = 'PanelHeader';

export const PanelTitle = forwardRef<HTMLSpanElement, PanelTitleProps>(({
  className, style, children, ...rest
}, ref) => {
  const { size, labelId } = useContext(PanelContext);
  return (
    <span
      ref={ref}
      {...rest}
      {...mergeExternal(
        stylex.props(styles.title, size === 'm' ? styles.titleM : styles.titleS),
        className,
        style,
      )}
    >
      {/* Labels the header toggle, so screen readers hear "Positions,
          collapsed" instead of the generic fallback name. */}
      <span id={labelId}>{children}</span>
    </span>
  );
});
PanelTitle.displayName = 'PanelTitle';

// Unmounts its children while the panel is collapsed. Unmounting (rather
// than hiding) is what makes the header the panel's :last-child, which drops
// the header divider — and it resets child state on collapse, like before
// when consumers wrote `{!collapsed && body}` themselves.
export const PanelBody = forwardRef<HTMLDivElement, PanelSectionProps>(({
  children, ...rest
}, ref) => {
  const { collapsed } = useContext(PanelContext);
  if (collapsed) return null;
  return <div ref={ref} {...rest}>{children}</div>;
});
PanelBody.displayName = 'PanelBody';

export const PanelFooter = forwardRef<HTMLDivElement, PanelSectionProps>(({
  className, style, children, ...rest
}, ref) => {
  // A collapsed panel shows only its header — footer actions (pagination
  // etc.) operate on the hidden body, so they collapse with it.
  const { collapsed } = useContext(PanelContext);
  if (collapsed) return null;
  return (
    <div ref={ref} {...rest} {...mergeExternal(stylex.props(styles.footer), className, style)}>
      {children}
    </div>
  );
});
PanelFooter.displayName = 'PanelFooter';
